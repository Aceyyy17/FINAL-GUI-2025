    private void editbuttonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_editbuttonMouseClicked
        int rowIndex = diagnoseTable.getSelectedRow();

        if(rowIndex < 0){
            JOptionPane.showMessageDialog(null, "Please Select an Item!");

        }else{
            try{

                dbConnector dbc = new dbConnector();
                TableModel tbl = diagnoseTable.getModel();
                ResultSet rs = dbc.getData("SELECT * FROM tbl_diagnose WHERE diagnose_id = '"+tbl. getValueAt(rowIndex, 0)+"'");
                if(rs.next()){

                    addDiagnose ad = new addDiagnose();
                    ad.patientID.setText(""+rs.getString("diagnose_id"));
                    ad.symptom.setText(""+rs.getString("d_name"));
                    ad.stat.setText(""+rs.getString("status"));
                    ad.treat.setText(""+rs.getString("treatment"));
                    ad.setVisible(true);
                    this.dispose();  

                }

            }catch(SQLException ex){

                System.out.println(""+ex);
            }
        }
    }//GEN-LAST:event_editbuttonMouseClicked

