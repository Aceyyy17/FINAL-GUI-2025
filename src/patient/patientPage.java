    private void editbuttonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_editbuttonMouseClicked
        int rowIndex = patientTable.getSelectedRow();

        if(rowIndex < 0){
            JOptionPane.showMessageDialog(null, "Please Select an Item!");

        }else{
            try{

                dbConnector dbc = new dbConnector();
                TableModel tbl = patientTable.getModel();
                ResultSet rs = dbc.getData("SELECT * FROM tbl_patients WHERE p_id = '"+tbl. getValueAt(rowIndex, 0)+"'");
                if(rs.next()){

                    patientForm pf = new patientForm();
                    pf.p_id.setText(""+rs.getString("p_id"));
                    pf.p_fname.setText(""+rs.getString("p_fname"));
                    pf.p_lname.setText(""+rs.getString("p_lname"));
                    pf.address.setText(""+rs.getString("p_address"));
                    pf.p_num.setText(""+rs.getString("p_pnumber"));
                    pf.gender.setSelectedItem(""+rs.getString("p_gender"));
                    pf.setVisible(true);
                    this.dispose();  

                }

            }catch(SQLException ex){

                System.out.println(""+ex);
            }
        }
    }//GEN-LAST:event_editbuttonMouseClicked

