public boolean recoverycheck() {
    dbConnector dbc = new dbConnector();

    try {
        
        String queryUser = "SELECT * FROM tbl_users WHERE u_username = '" + un.getText() + "'";
        ResultSet resultUser = dbc.getData(queryUser);

        if (resultUser.next()) {
            uname = resultUser.getString("u_username");
            uid = resultUser.getString("u_id");

           
            String queryRecovery = "SELECT * FROM tbl_recover WHERE u_id = '" + uid + "'";
            ResultSet resultRecovery = dbc.getData(queryRecovery);

            if (resultRecovery.next()) {
                q1 = resultRecovery.getString("question1");
                q2 = resultRecovery.getString("question2");
                

                return true;
            } else {
                JOptionPane.showMessageDialog(null, "No recovery data found for this user.");   
                return false;
            }

        } else {
            JOptionPane.showMessageDialog(null, "Username not found.");
            return false;
        }

    } catch (SQLException ex) {
        System.out.println("Error: " + ex);
        return false;
    }
}
    
    
