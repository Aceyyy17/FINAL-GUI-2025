    private void saveMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_saveMouseClicked
            dbConnector dbc = new dbConnector();
            Session ses = Session.getInstance();
            
            try{
            int uid = ses.getUid();
        if(conpass.getText().isEmpty()||newpass.getText().isEmpty()){
            message.setText("Fill all field");
            } else if(!(conpass.getText().equals(newpass.getText()))){
            message.setText("password & confirm password should match");
            } else{
                 String pass = passwordHasher.hashPassword(newpass.getText());
            dbc.insertData("UPDATE tbl_users SET u_password ='"+pass+"'WHERE u_id ='"+uid+"'");
            System.out.println("updated successfully!");
            this.dispose();
            loginForm lf = new loginForm();
            lf.setVisible(true);
            
            }
      }catch(NoSuchAlgorithmException ex){
                    System.out.println(""+ex);
      }
    }//GEN-LAST:event_saveMouseClicked

